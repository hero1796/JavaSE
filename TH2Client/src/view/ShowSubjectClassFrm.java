    public void displayResult(ArrayList<SubjectClass> listStudent) {
        DefaultTableModel model = (DefaultTableModel) tblResult.getModel();
        model.getDataVector().removeAllElements();
        tblResult.repaint();
        for (SubjectClass st : listStudent) {
            model.addRow(new Object[]{st.getId(), st.getName(), st.getLearnTime(), st.getRoom(), st.getMaxSlot()});
        }
    }
    
